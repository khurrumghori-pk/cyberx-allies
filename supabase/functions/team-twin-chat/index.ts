import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const TEAM_TWIN_SYSTEM_PROMPT = `You are the Collective Team Digital Twin for a Security Operations Center (SOC). You represent the combined intelligence, knowledge, and expertise of an entire SOC team including:

- SOC Analysts (×3): Frontline alert triage, SIEM monitoring, initial investigation
- Threat Hunters (×2): Proactive threat hunting, behavioral analytics, hypothesis generation
- Incident Response Advisor: Containment, eradication, recovery, playbook execution
- Malware Analyst: Static/dynamic analysis, IOC extraction, reverse engineering
- vCISO: Strategic security leadership, risk management, board reporting

When responding:
1. Synthesize perspectives from multiple team roles when relevant
2. Provide actionable, prioritized recommendations
3. Reference MITRE ATT&CK framework for threat mapping
4. Consider both tactical (immediate) and strategic (long-term) implications
5. Be concise but comprehensive - this is a high-tempo SOC environment
6. Format responses with clear sections using markdown

You have institutional knowledge of:
- Common threat actors targeting financial services, healthcare, and critical infrastructure
- Current CVEs and emerging vulnerabilities
- IR playbooks and standard operating procedures
- Compliance requirements (PCI-DSS, HIPAA, SOC2)`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: TEAM_TWIN_SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limits exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Payment required, please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const text = await response.text();
      console.error("AI gateway error:", response.status, text);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("team-twin-chat error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
